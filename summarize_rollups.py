"""
summarize_rollups.py — Phase 2 of the OneNote summarisation pipeline.

Reads page summaries from 03_Summaries and synthesises groups of related
pages into rollup summaries, using the manifest level/order hierarchy to
know which pages belong together.

Three rollup levels (all generated by default):

  1. Sub-group  — level-0 parent page + its level-1 children
                  e.g. "Annual Security Reports" synthesising M-trends + DBIR
                  Output: 03_Summaries/…/Section/_rollup_ParentName.md

  2. Section    — all sub-group rollups (or page summaries) within a section
                  Output: 03_Summaries/…/Section/_section_rollup.md

  3. Notebook   — all section rollups for a notebook
                  Output: 03_Summaries/Notebook/_notebook_rollup.md

Regeneration criteria (per rollup file):
  • File does not exist
  • Child count changed  (page added or removed)
  • Children hash changed (different set of child pages)
  • Any child summary has a newer 'summarised' date than the rollup
  • --force flag

Prerequisites:
  • Run patch_manifest_hierarchy.py first to populate level/order in manifests.
  • Run summarize_pages.py first to generate the page summaries.
"""

