total_generated  = 0
total_skipped    = 0
total_errors     = 0
processing_times = []
script_start     = time.time()

for notebook in selected_nbs:
    nb_dir     = RAW_DIR / notebook
    manifest   = load_manifest(nb_dir)
    pages_meta = manifest.get("pages", {})

    if not pages_meta:
        log(f"[Skip — no manifest]: {notebook}\n")
        continue

    has_hierarchy = any(v.get("level", 0) > 0 for v in pages_meta.values())
    if not has_hierarchy:
        log(f"[Skip — no hierarchy data]: {notebook}")
        log(f"  → Run patch_manifest_hierarchy.py first, then retry.\n")
        continue

    log(f"Notebook: {notebook}")
    section_map          = build_section_map(pages_meta)
    section_rollup_paths = []   # accumulated for notebook rollup

    for sec_key, pages in sorted(section_map.items()):

        # Skip sections handled elsewhere (e.g. Candidates)
        leaf_section = sec_key.split("/")[-1].lower() if sec_key else ""
        if leaf_section in SKIP_SECTIONS:
            log(f"  [Skip section — {leaf_section}]: {sec_key}")
            continue

        section_display = sec_key.replace("/", " / ") if sec_key else notebook
        section_folder  = SUM_DIR / notebook / sec_key if sec_key else SUM_DIR / notebook

        groups = group_by_parent(pages)

        # ----------------------------------------------------------------
        # LEVEL 1 — SUB-GROUP ROLLUPS
        # Each level-0 parent with ≥ MIN_CHILDREN level-1 children
        # ----------------------------------------------------------------
        # section_inputs: what to feed into the section rollup.
        # Prefer sub-group rollups; fall back to raw page summaries.
        section_inputs = []

        if DO_SUBGROUP:
            for g in groups:
                parent   = g["parent"]
                children = g["children"]

                if not parent:
                    # Orphaned sub-pages — include their summaries in section
                    for c in children:
                        p = section_folder / f"{c['name']}.md"
                        if p.exists():
                            section_inputs.append(p)
                    continue

                parent_sum = section_folder / f"{parent['name']}.md"

                if len(children) < MIN_CHILDREN:
                    # Not enough children — use parent summary directly
                    if parent_sum.exists():
                        section_inputs.append(parent_sum)
                    continue

                child_names  = [c["name"] for c in children]
                child_paths  = [section_folder / f"{n}.md" for n in child_names]
                available    = [p for p in child_paths if p.exists()]

                if len(available) < MIN_CHILDREN:
                    log(f"  [Skip subgroup — only {len(available)} summaries found]: {parent['name']}")
                    if parent_sum.exists():
                        section_inputs.append(parent_sum)
                    continue

                rollup_path = section_folder / f"_rollup_{parent['name']}.md"

                # Include parent's own summary as the first block (if it has content)
                input_paths = ([parent_sum] if parent_sum.exists() else []) + available
                input_names = (
                    ([parent["name"] + " (overview)"] if parent_sum.exists() else [])
                    + child_names
                )

                t0 = time.time()
                result = run_rollup(
                    SUBGROUP_PROMPT,
                    {"parent": parent["name"]},
                    rollup_path, "subgroup",
                    notebook, section_display,
                    f"Rollup: {parent['name']}",
                    input_paths, input_names,
                )
                elapsed = time.time() - t0

                if result is not None:
                    section_inputs.append(result)
                    if rollup_path.exists():
                        # Only count as generated if it was freshly written
                        meta, _ = parse_frontmatter(rollup_path.read_text(encoding="utf-8"))
                        if meta.get("summarised", "") == datetime.now().strftime("%Y-%m-%d"):
                            total_generated += 1
                            processing_times.append(elapsed)
                        else:
                            total_skipped += 1
                else:
                    total_errors += 1
                    if parent_sum.exists():
                        section_inputs.append(parent_sum)   # degrade gracefully

        else:
            # Sub-group level not requested — collect all page summaries for section rollup
            for p in pages:
                sp = section_folder / f"{p['name']}.md"
                if sp.exists():
                    section_inputs.append(sp)

        # ----------------------------------------------------------------
        # LEVEL 2 — SECTION ROLLUP
        # ----------------------------------------------------------------
        if DO_SECTION:
            inputs = [p for p in section_inputs if p.exists()]

            if len(inputs) < MIN_CHILDREN:
                log(f"  [Skip section rollup — only {len(inputs)} inputs]: {sec_key or notebook}")
            else:
                child_names = [p.stem for p in inputs]
                rollup_path = section_folder / "_section_rollup.md"

                t0 = time.time()
                result = run_rollup(
                    SECTION_PROMPT,
                    {"section": section_display},
                    rollup_path, "section",
                    notebook, section_display,
                    f"Section Summary: {section_display}",
                    inputs, child_names,
                )
                elapsed = time.time() - t0

                if result is not None:
                    section_rollup_paths.append(result)
                    if rollup_path.exists():
                        meta, _ = parse_frontmatter(rollup_path.read_text(encoding="utf-8"))
                        if meta.get("summarised", "") == datetime.now().strftime("%Y-%m-%d"):
                            total_generated += 1
                            processing_times.append(elapsed)
                        else:
                            total_skipped += 1
                else:
                    total_errors += 1

    # --------------------------------------------------------------------
    # LEVEL 3 — NOTEBOOK ROLLUP
    # --------------------------------------------------------------------
    if DO_NOTEBOOK:
        inputs = [p for p in section_rollup_paths if p.exists()]

        if len(inputs) < MIN_CHILDREN:
            log(f"  [Skip notebook rollup — only {len(inputs)} section rollups]: {notebook}")
        else:
            child_names = [p.parent.name for p in inputs]   # section folder names
            rollup_path = SUM_DIR / notebook / "_notebook_rollup.md"

            t0 = time.time()
            result = run_rollup(
                NOTEBOOK_PROMPT,
                {"notebook": notebook},
                rollup_path, "notebook",
                notebook, "",
                f"Notebook Summary: {notebook}",
                inputs, child_names,
            )
            elapsed = time.time() - t0

            if result is not None:
                if rollup_path.exists():
                    meta, _ = parse_frontmatter(rollup_path.read_text(encoding="utf-8"))
                    if meta.get("summarised", "") == datetime.now().strftime("%Y-%m-%d"):
                        total_generated += 1
                        processing_times.append(elapsed)
                    else:
                        total_skipped += 1
            else:
                total_errors += 1

    log()

# ---------------------------------------------------------------------------
