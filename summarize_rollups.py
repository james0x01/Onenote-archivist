def load_manifest(nb_dir):
    path = nb_dir / "manifest.json"
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except Exception:
        return {"pages": {}}


def build_section_map(pages_meta):
    """
    Group manifest pages by section key (everything before the last '/').
    Each group is a list of page dicts sorted by 'order'.

    Returns: {section_key: [{"name", "key", "level", "order"}, ...]}
    """
    sections = {}
    for key, meta in pages_meta.items():
        parts = key.rsplit("/", 1)
        sec_key   = parts[0] if len(parts) == 2 else ""
        page_name = parts[-1]
        sections.setdefault(sec_key, []).append({
            "name":  page_name,
            "key":   key,
            "level": meta.get("level", 0),
            "order": meta.get("order", 0),
        })
    for pages in sections.values():
        pages.sort(key=lambda p: p["order"])
    return sections


def group_by_parent(pages):
    """
    Walk a list of pages sorted by order and group level-1 sub-pages under
    the level-0 parent that precedes them.

    Returns: [{"parent": page_dict_or_None, "children": [page_dict, ...]}]

    • level-0 pages → new group, may accumulate children
    • level-1 pages → appended to current group's children
    • Orphaned level-1 pages (no preceding level-0) → group with parent=None
    """
    groups  = []
    current = None
    for p in pages:
        if p["level"] == 0:
            current = {"parent": p, "children": []}
            groups.append(current)
        else:
            if current is None:
                current = {"parent": None, "children": []}
                groups.append(current)
            current["children"].append(p)
    return groups

