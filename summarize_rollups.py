def call_ollama(prompt):
    try:
        resp = requests.post(
            f"{OLLAMA_HOST}/api/generate",
            json={"model": _llm_model_name, "prompt": prompt, "stream": False},
            timeout=OLLAMA_TIMEOUT,
        )
        resp.raise_for_status()
        return resp.json()["response"].strip()
    except requests.exceptions.Timeout:
        log(f"    [Error]: Ollama timed out after {OLLAMA_TIMEOUT}s")
        return None
    except Exception as e:
        log(f"    [Error]: {e}")
        return None


def call_gemini(prompt):
    time.sleep(GEMINI_CALL_DELAY)
    wait = 20
    for attempt in range(3):
        try:
            return _gemini_client.models.generate_content(
                model=_llm_model_name, contents=prompt
            ).text.strip()
        except Exception as e:
            if "503" in str(e) and attempt < 2:
                log(f"    [Retry {attempt+1}/3 in {wait}s]: {e}")
                time.sleep(wait)
                wait = min(wait + 20, 60)
            else:
                log(f"    [Error]: {e}")
                return None
    return None


def call_llm(prompt):
    return call_gemini(prompt) if _llm_backend == "gemini" else call_ollama(prompt)


def run_rollup(prompt_template, format_args, rollup_path, rollup_type,
               notebook, section, title, child_paths, child_names):
    """
    Check freshness, call LLM, write rollup.
    Returns the rollup_path if successful/skipped, None on error.
    """
    do_it, reason = needs_update(rollup_path, child_paths, child_names, force)

    if not do_it:
        log(f"    [Skip — {reason}]: {rollup_path.name}")
        return rollup_path   # return path so parent level can include it

    log(f"    Generating [{reason}]: {rollup_path.name}")

    # Build content blocks from each input file
    blocks = []
    for cp in child_paths:
        if not cp.exists():
            continue
        _, body = parse_frontmatter(cp.read_text(encoding="utf-8"))
        if body.strip():
            blocks.append(f"## {cp.stem}\n\n{body}")

    if len(blocks) < MIN_CHILDREN:
        log(f"    [Skip — fewer than {MIN_CHILDREN} summaries available]")
        return None

    combined = "\n\n---\n\n".join(blocks)
    prompt   = prompt_template.format(**format_args, content=combined)

    t_start  = time.time()
    response = call_llm(prompt)
    elapsed  = time.time() - t_start

    if response is None:
        return None

    summary, llm_tags = parse_tags(response)

    # Auto-tags: notebook + section + type marker
    auto_tags = [notebook.lower().replace(" ", "-")]
    if section:
        auto_tags.append(section.split(" / ")[-1].lower().replace(" ", "-"))
    auto_tags.append(rollup_type.replace("_", "-"))
    all_tags = auto_tags + [t for t in llm_tags if t not in auto_tags]

    write_rollup(rollup_path, rollup_type, notebook, section, title,
                 summary, all_tags, child_names)

    log(f"      [{elapsed:.1f}s | {len(combined):,} chars input]")
    return rollup_path

