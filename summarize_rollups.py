def format_eta(seconds):
    if seconds < 60:   return f"{int(seconds)}s"
    if seconds < 3600: return f"{int(seconds // 60)}m {int(seconds % 60)}s"
    return f"{int(seconds // 3600)}h {int((seconds % 3600) // 60)}m"


def parse_frontmatter(text):
    meta, body = {}, text
    if text.startswith("---"):
        end = text.find("---", 3)
        if end != -1:
            for line in text[3:end].splitlines():
                if ":" in line:
                    k, _, v = line.partition(":")
                    meta[k.strip()] = v.strip().strip('"')
            body = text[end + 3:].strip()
    return meta, body


def parse_tags(response):
    body_lines, tags = [], []
    for line in response.strip().splitlines():
        if line.strip().upper().startswith("TAGS:"):
            raw  = line.strip().split(":", 1)[1]
            tags = [t.strip().lower().replace(" ", "-") for t in raw.split(",") if t.strip()]
        else:
            body_lines.append(line)
    return "\n".join(body_lines).strip(), tags


def fingerprint(names):
    """Short MD5 of a sorted list of names — detects additions and removals."""
    h = hashlib.md5()
    for n in sorted(names):
        h.update(n.encode())
    return h.hexdigest()[:8]


def needs_update(rollup_path, child_paths, child_names, force):
    """
    Return (should_regenerate: bool, reason: str).

    Checks in order:
      1. force flag or file missing
      2. child count or set changed
      3. any child summary newer than this rollup
    """
    if force:
        return True, "force"
    if not rollup_path.exists():
        return True, "new"

    try:
        meta, _ = parse_frontmatter(rollup_path.read_text(encoding="utf-8"))
    except Exception:
        return True, "unreadable"

    # --- fingerprint check ---
    cur_hash  = fingerprint(child_names)
    cur_count = len(child_names)
    if meta.get("children_hash", "") != cur_hash:
        return True, "children changed"
    if int(meta.get("child_count", 0)) != cur_count:
        return True, "child count changed"

    # --- timestamp check ---
    stored_date = meta.get("summarised", "")
    if stored_date:
        try:
            rollup_dt = datetime.fromisoformat(stored_date)
            for cp in child_paths:
                if cp.exists():
                    cm, _ = parse_frontmatter(cp.read_text(encoding="utf-8"))
                    cd = cm.get("summarised", "")
                    if cd and datetime.fromisoformat(cd) > rollup_dt:
                        return True, f"child updated ({cp.stem})"
        except Exception:
            return True, "date parse error"

    return False, "up to date"


def write_rollup(rollup_path, rollup_type, notebook, section, title,
                 summary, tags, child_names):
    """Write a rollup file with full frontmatter."""
    tag_str = "[" + ", ".join(tags) + "]"
    fm = [
        "---",
        f'notebook: "{notebook}"',
    ]
    if section:
        fm.append(f'section: "{section}"')
    fm += [
        f'title: "{title}"',
        f'rollup_type: "{rollup_type}"',
        f'child_count: {len(child_names)}',
        f'children_hash: "{fingerprint(child_names)}"',
        f'tags: {tag_str}',
        f'summarised: "{datetime.now().strftime("%Y-%m-%d")}"',
        f'summarised_by: "{_llm_model_name}"',
        "---\n",
    ]
    rollup_path.parent.mkdir(parents=True, exist_ok=True)
    rollup_path.write_text("\n".join(fm) + f"# {title}\n\n{summary}\n", encoding="utf-8")

